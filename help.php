    <h2> How To Find A Course </h2> 

   <ul> 
     <li> Click on the "Find Course" button. </li> 
     <li> Select the course you are looking for in the database. </li> 
     <li> You can also enter the class information manually by subject number, course number, course number, or amount of credits. </li> 
   </ul>  

   <h2> How To Add A Course </h2> 
   
   <ul> 
     <li> Select the class you want to add through the "Find Course" button, or insert the class you want manually. </li> 
     <li> Once you have the class, press the "Add Course" button. </li> 
   </ul>  

   <h2> How To Delete A Course </h2> 
   
   <ul>
     <li> Select the class you want to delete on your "My Courses" list. </li> 
     <li> Once you have the class selected (the information will display within the course search), press the "Delete Course" button to remove the class from the "My Courses" list. </li> 
   </ul>  
   
   <h2> How To Clear Courses </h2> 
   
   <ul> 
     <li> To clear all courses from your "My Courses" list, press the "Clear Courses" button. </li> 
     <li> Answer the prompt if you are sure. </li> 
   </ul> 
   
   <h2> How To Show Progress </h2>  
   
   <ul> 
     <li> When you want to see your progress, click on the "Show Progress" button. </li> 
     <li> Once done, it will display your current progress based on the classes you have in your "My Courses" list. </li> 
   </ul>  

   <h2> How To Show Gen Ed Options </h2> 
   
   <ul> 
     <li> Click on a course through the "Find Course" button. </li> 
     <li> Once a course is selected, click on the "Show GenEd Options" button. </li> 
     <li> You will see an extended list with Gen-ed courses where there will be either a one or zero next to it. If there is a "1" next to it, this means it counts as a gen-ed credit. </li> 
   </ul> 
 
    
